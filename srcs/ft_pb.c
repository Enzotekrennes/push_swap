#include "push_swap.h"

void		ft_pb(t_pile *t)
{
	t_liste *tmp;

	if (t->a)
	{
		tmp = t->a->next;
		t->a->next = t->b;
		t->b = t->a;
		t->a = tmp;
	}
}
