#include "push_swap.h"

void		ft_sa(t_pile *t)
{
	t_liste *tmp;

	if (t->a && t->a->next)
	{
		tmp = t->a->next;
		t->a->next = t->a->next->next;
		tmp->next = t->a;
		t->a = tmp;
	}
}
