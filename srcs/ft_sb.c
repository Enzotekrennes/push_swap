#include "push_swap.h"

void		ft_sb(t_pile *t)
{
	t_liste *tmp;

	if (t->b && t->b->next)
	{
		tmp = t->b->next;
		t->b->next = t->b->next->next;
		tmp->next = t->b;
		t->b = tmp;
	}
}
